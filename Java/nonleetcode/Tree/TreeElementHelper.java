public class TreeElementHelper {

    private int element;

    public TreeElementHelper() {
        this.element = 0;
    }

    public TreeElementHelper(int value) {
        this.element = value;
    }

    public int getElement() {
        return element;
    }

    public void setElement(int element) {
        this.element = element;
    }
}
